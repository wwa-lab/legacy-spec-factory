def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def load_yaml(path: Path) -> Any:
    try:
        import yaml  # type: ignore
    except ImportError as exc:  # pragma: no cover - depends on local runtime
        raise RuntimeError(
            "PyYAML is required to validate YAML sidecar declarations. "
            "Install with: pip install pyyaml"
        ) from exc
    with path.open("r", encoding="utf-8") as handle:
        return yaml.safe_load(handle)


def artifact_program_prefix(program_name: str) -> str:
    prefix = ARTIFACT_SAFE_RE.sub("_", program_name.strip().upper())
    prefix = prefix.strip("._-")
    return prefix or "PROGRAM"


def find_artifact_path(analysis_dir: Path, base_name: str) -> Path | None:
    prefixed_matches = sorted(analysis_dir.glob(f"*-{base_name}"))
    if len(prefixed_matches) == 1:
        return prefixed_matches[0]
    generic_path = analysis_dir / base_name
    if generic_path.is_file():
        return generic_path
    return None


def find_program_analysis_path(analysis_dir: Path, explicit_path: Path | None) -> Path | None:
    if explicit_path is not None:
        return explicit_path
    prefixed_matches = sorted(analysis_dir.glob("*-program-analysis.md"))
    if len(prefixed_matches) == 1:
        return prefixed_matches[0]
    default_path = analysis_dir / "program-analysis.md"
    if default_path.is_file():
        return default_path
    matches = sorted(analysis_dir.glob("program-analysis-*.md"))
    if len(matches) == 1:
        return matches[0]
    return None


def summary_path(analysis_dir: Path) -> Path | None:
    return find_artifact_path(analysis_dir, "program-analysis-summary.yaml")


def sidecar_entries_for_dir(analysis_dir: Path) -> dict[str, dict[str, str]]:
    path = summary_path(analysis_dir)
    if path is None or not path.is_file():
        return {}
    payload = load_yaml(path)
    sidecars = (payload or {}).get("sidecars", {})
    if not isinstance(sidecars, dict):
        return {}
    entries: dict[str, dict[str, str]] = {}
    for key, value in sidecars.items():
        if not isinstance(value, dict):
            continue
        entries[str(key)] = {
            "path": str(value.get("path", "")),
            "status": str(value.get("status", "")),
        }
    return entries


def declared_sidecar_path(
    analysis_dir: Path,
    sidecar_key: str,
    fallback_base_name: str,
) -> Path | None:
    entry = sidecar_entries_for_dir(analysis_dir).get(sidecar_key)
    if entry:
        rel_path = entry.get("path", "")
        if rel_path:
            candidate = analysis_dir / rel_path
            if candidate.is_file():
                return candidate
    return find_artifact_path(analysis_dir, fallback_base_name)


def h2_positions(markdown: str) -> dict[str, int]:
    positions: dict[str, int] = {}
    for match in re.finditer(r"^##\s+(.+?)\s*$", markdown, re.M):
        heading = match.group(1).strip()
        positions.setdefault(heading, match.start())
    return positions


def heading_positions(markdown: str) -> dict[str, int]:
    positions: dict[str, int] = {}
    for match in re.finditer(r"^#{2,6}\s+(.+?)\s*$", markdown, re.M):
        heading = match.group(1).strip()
        positions.setdefault(heading, match.start())
    return positions


def heading_spans(markdown: str) -> dict[str, tuple[int, int]]:
    matches = list(re.finditer(r"^(#{2,6})\s+(.+?)\s*$", markdown, re.M))
    spans: dict[str, tuple[int, int]] = {}
    for index, match in enumerate(matches):
        heading = match.group(2).strip()
        start = match.end()
        end = matches[index + 1].start() if index + 1 < len(matches) else len(markdown)
        spans.setdefault(heading, (start, end))
    return spans


def h2_section_text(markdown: str, heading: str) -> str:
    pattern = re.compile(r"^##\s+(.+?)\s*$", re.M)
    matches = list(pattern.finditer(markdown))
    for index, match in enumerate(matches):
        if match.group(1).strip() != heading:
            continue
        start = match.end()
        end = matches[index + 1].start() if index + 1 < len(matches) else len(markdown)
        return markdown[start:end]
    return ""


def h3_blocks(section_text: str) -> list[tuple[str, int, str]]:
    matches = list(re.finditer(r"^###\s+(.+?)\s*$", section_text, re.M))
    blocks: list[tuple[str, int, str]] = []
    for index, match in enumerate(matches):
        start = match.end()
        end = matches[index + 1].start() if index + 1 < len(matches) else len(section_text)
        blocks.append((match.group(1).strip(), match.start(), section_text[start:end]))
    return blocks


def validate_ordered_headings(
    markdown: str,
    required_headings: tuple[str, ...],
    artifact_label: str,
) -> list[str]:
    findings: list[str] = []
    positions = heading_positions(markdown)
    missing = [heading for heading in required_headings if heading not in positions]
    if missing:
        findings.append(f"{artifact_label} missing required headings: " + ", ".join(missing))
        return findings
    ordered_positions = [positions[heading] for heading in required_headings]
    if ordered_positions != sorted(ordered_positions):
        findings.append(f"{artifact_label} required headings are out of order")
    return findings


