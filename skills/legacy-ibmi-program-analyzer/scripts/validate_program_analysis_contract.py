def batch_detail_files(analysis_dir: Path) -> list[Path]:
    declared = [
        analysis_dir / entry["path"]
        for key, entry in sidecar_entries_for_dir(analysis_dir).items()
        if key.startswith("routine_logic_deep_read_batch_")
        and entry.get("path")
        and (analysis_dir / entry["path"]).is_file()
    ]
    batch_dir = analysis_dir / "routine-logic-details"
    patterns = ("part-*.md", "*-part-*.md", "deep-read-batch-*.md", "*-deep-read-batch-*.md", "deep-batch-*.md", "*-deep-batch-*.md")
    files: list[Path] = list(declared)
    if batch_dir.is_dir():
        for pattern in patterns:
            files.extend(batch_dir.glob(pattern))
    return sorted(
        set(files),
        key=lambda path: tuple(
            (0, int(part)) if part.isdigit() else (1, part.lower())
            for part in re.split(r"(\d+)", path.as_posix())
        ),
    )


def semantic_value_is_complete(value: Any, minimum_words: int = 2) -> bool:
    if value is None or value == "" or value == [] or value == {}:
        return False
    text = str(value)
    return not semantic_seed_matches(text) and meaningful_word_count(text) >= minimum_words


def batch_coverage_column_ids(
    batch_text: str,
    accepted_headers: set[str],
    id_pattern: re.Pattern[str],
) -> list[str]:
    rows = markdown_table_rows(h2_section_text(batch_text, "Batch Coverage Summary"))
    if not rows:
        return []
    headers = [strip_inline_code_marks(cell).strip("*_ ").lower() for cell in rows[0]]
    column = next(
        (index for index, header in enumerate(headers) if header in accepted_headers),
        None,
    )
    if column is None:
        return []
    identifiers: list[str] = []
    for row in rows[1:]:
        if column >= len(row):
            continue
        identifiers.extend(match.group(0).upper() for match in id_pattern.finditer(row[column]))
    return list(dict.fromkeys(identifiers))


def batch_assigned_rlog_ids(batch_path: Path) -> list[str]:
    batch_text = read_text(batch_path)
    coverage_ids = batch_coverage_column_ids(
        batch_text,
        {"rlog detail", "rlog", "detail link"},
        RLOG_RE,
    )
    routine_section = h2_section_text(batch_text, "Routine Details")
    detail_ids = list(extract_rlog_detail_blocks(routine_section))
    return list(dict.fromkeys([*coverage_ids, *detail_ids]))


def batch_assigned_window_ids(batch_text: str) -> list[str]:
    return batch_coverage_column_ids(
        batch_text,
        {"window id", "deep-read window", "deep read window"},
        DEEP_READ_WINDOW_RE,
    )


def validate_batch_yaml_semantic_completion(analysis_dir: Path) -> list[str]:
    batches = batch_detail_files(analysis_dir)
    if not batches:
        return []
    yaml_path = declared_sidecar_path(
        analysis_dir,
        "routine_logic_details_yaml",
        "routine-logic-details.yaml",
    )
    if yaml_path is None or not yaml_path.is_file():
        return []

    payload = load_yaml(yaml_path)
    inventory = (payload or {}).get("routine_logic_inventory", {})
    if not isinstance(inventory, dict):
        return []
    detail_entries = inventory.get("details", [])
    summary_entries = inventory.get("summary", [])
    details_by_id = {
        str(entry.get("detail_id") or "").upper(): entry
        for entry in detail_entries
        if isinstance(entry, dict) and entry.get("detail_id")
    } if isinstance(detail_entries, list) else {}
    summary_by_id = {
        str(entry.get("detail_ref") or "").upper(): entry
        for entry in summary_entries
        if isinstance(entry, dict) and entry.get("detail_ref")
    } if isinstance(summary_entries, list) else {}

    assigned_ids: list[str] = []
    for batch_path in batches:
        assigned_ids.extend(batch_assigned_rlog_ids(batch_path))

    findings: list[str] = []
    for rlog_id in dict.fromkeys(assigned_ids):
        detail = details_by_id.get(rlog_id)
        if detail is None:
            findings.append(
                f"{yaml_path.name} missing details[] semantic record for batched {rlog_id}"
            )
            continue
        semantic_status = str(detail.get("semantic_status") or "").strip().lower()
        coverage = str(detail.get("coverage") or "").strip().lower()
        if semantic_status != DEEP_READ_COMPLETE_STATUS:
            findings.append(
                f"{yaml_path.name} batched {rlog_id} requires "
                f"semantic_status: {DEEP_READ_COMPLETE_STATUS}, found {semantic_status or 'missing'}"
            )
        if coverage != "deep_read":
            findings.append(
                f"{yaml_path.name} batched {rlog_id} requires coverage: deep_read, "
                f"found {coverage or 'missing'}"
            )
        if not semantic_value_is_complete(detail.get("execution_trigger"), minimum_words=3):
            findings.append(
                f"{yaml_path.name} batched {rlog_id} needs a source-backed execution_trigger"
            )
        incomplete_fields = [
            field
            for field in BATCH_STRUCTURED_SEMANTIC_FIELDS
            if not semantic_value_is_complete(detail.get(field))
        ]
        if incomplete_fields:
            findings.append(
                f"{yaml_path.name} batched {rlog_id} has incomplete structured semantic "
                "field(s): " + ", ".join(incomplete_fields)
            )

        summary = summary_by_id.get(rlog_id)
        if summary is None:
            findings.append(
                f"{yaml_path.name} missing summary[] mirror for batched {rlog_id}"
            )
            continue
        summary_status = str(summary.get("semantic_status") or "").strip().lower()
        summary_coverage = str(summary.get("coverage") or "").strip().lower()
        if summary_status != semantic_status or summary_coverage != coverage:
            findings.append(
                f"{yaml_path.name} summary[] and details[] semantic state disagree for "
                f"batched {rlog_id}"
            )
    return findings


