def requires_large_program_batches(payload: dict[str, Any]) -> bool:
    if payload.get("program_size_tier") == "large_extreme_program":
        return True
    counts = payload.get("counts", {})
    if isinstance(counts, dict) and int(counts.get("source_lines", 0) or 0) > 10000:
        return True
    source = payload.get("source", {})
    if isinstance(source, dict) and int(source.get("line_count", 0) or 0) > 10000:
        return True
    return False


def validate_large_program_batches(analysis_dir: Path) -> list[str]:
    payload = summary_payload(analysis_dir)
    if not payload or not requires_large_program_batches(payload):
        return []

    findings: list[str] = []
    batches = batch_detail_files(analysis_dir)
    if not batches:
        findings.append(
            "large_extreme_program requires routine-logic-details/deep-read-batch-*.md "
            "batch checkpoint files"
        )
        return findings

    program = str(payload.get("program") or "").strip()
    batch_dir = analysis_dir / "routine-logic-details"
    prefix = artifact_program_prefix(program) if program else ""
    canonical_first = batch_dir / f"{prefix}-deep-read-batch-001.md" if prefix else None
    alias_first = batch_dir / f"{prefix}-deep-batch-001.md" if prefix else None
    legacy_first = analysis_dir / "routine-logic-details" / "deep-read-batch-001.md"
    wildcard_first = sorted(batch_dir.glob("*-deep-read-batch-001.md")) if not prefix else []
    canonical_ok = canonical_first is not None and canonical_first.is_file()
    alias_ok = alias_first is not None and alias_first.is_file()
    if not canonical_ok and not alias_ok and not wildcard_first:
        if legacy_first.is_file():
            return findings
        findings.append(
            "large_extreme_program requires a first batch checkpoint: "
            "routine-logic-details/<PROGRAM>-deep-read-batch-001.md"
        )
    return findings


def validate_routine_detail_review_surfaces(analysis_dir: Path) -> list[str]:
    findings: list[str] = []
    batches = batch_detail_files(analysis_dir)
    if not batches:
        return findings

    routine_markdown_path = declared_sidecar_path(
        analysis_dir,
        "routine_logic_details",
        "routine-logic-details.md",
    )
    if routine_markdown_path is not None and routine_markdown_path.is_file():
        findings.extend(
            validate_ordered_headings(
                read_text(routine_markdown_path),
                ROUTINE_FINAL_SECTIONS,
                "routine-logic-details.md",
            )
        )

    for batch_path in batches:
        batch_text = read_text(batch_path)
        batch_label = str(batch_path.relative_to(analysis_dir))
        window_ids = batch_assigned_window_ids(batch_text)
        assigned_ids = batch_assigned_rlog_ids(batch_path)
        scope_count = max(len(window_ids), len(assigned_ids))
        if scope_count > 5:
            findings.append(
                f"{batch_label} contains more than 5 deep-read routines/windows ({scope_count})"
            )
        seed_matches = semantic_seed_matches(batch_text)
        if seed_matches:
            findings.append(
                f"{batch_label} still contains pending semantic deep-read content; "
                "complete this retained batch before validation. Matched: "
                + ", ".join(seed_matches)
            )
        findings.extend(
            validate_ordered_h2_headings(
                batch_text,
                BATCH_REQUIRED_SECTIONS,
                batch_label,
            )
        )
        findings.extend(validate_batch_core_has_no_source_snippets(batch_text, batch_label))
        findings.extend(validate_batch_core_has_no_pending_cells(batch_text, batch_label))
        routine_section = h2_section_text(batch_text, "Routine Details")
        findings.extend(
            validate_rlog_block_quality(
                routine_section,
                assigned_ids or None,
                batch_label,
            )
        )
        positions = heading_positions(batch_text)
        if not all(heading in positions for heading in BATCH_CORE_SECTIONS):
            continue
        first_core_position = min(positions[heading] for heading in BATCH_CORE_SECTIONS)
        rlog_match = RLOG_RE.search(batch_text)
        if rlog_match and first_core_position > rlog_match.start():
            findings.append(
                f"{batch_label} must put Calculation/Validation/Exception core logic before per-routine RLOG detail"
            )
    return findings


def validate_sidecar_set(analysis_dir: Path) -> list[str]:
    findings: list[str] = []
    path = summary_path(analysis_dir)
    if path is None or not path.is_file():
        findings.append("Missing required file: <PROGRAM>-program-analysis-summary.yaml")
        return findings

    entries = sidecar_entries_for_dir(analysis_dir)
    for key in CORE_SIDECAR_KEYS:
        entry = entries.get(key)
        if not entry:
            findings.append(f"{path.name} missing sidecar declaration: {key}")
            continue
        rel_path = entry.get("path")
        status = entry.get("status")
        if status != "present":
            findings.append(f"core sidecar {key} must have status present, found {status or 'missing'}")
        if rel_path and not (analysis_dir / rel_path).is_file():
            findings.append(f"declared core sidecar file is missing: {rel_path}")

    for key, entry in entries.items():
        status = entry.get("status", "")
        rel_path = entry.get("path", "")
        if status in {"present", "optional_triggered"} and rel_path and not (analysis_dir / rel_path).is_file():
            findings.append(f"declared sidecar {key} is {status} but file is missing: {rel_path}")

    return findings


